def add(a: int | float, b: int | float) -> int | float:
    """This function takes arguments of two numbers: int or float, and returns the addition of two numbers"""
    return a + b


def sub(a: int | float, b: int | float) -> int | float:
    """This function takes arguments of two numbers: int or float, and return the subtraction of two numbers"""
    return a - b


def multiplication(a: int | float, b: int | float) -> int | float:
    """This function takes arguments of two numbers: int or float, and return the multiplication of two numbers"""
    return a * b


def division(a: int | float, b: int | float) -> int | float:
    """This function takes arguments of two numbers: int or float, and return the division of two numbers"""
    return a / b



